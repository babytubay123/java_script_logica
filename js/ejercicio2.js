/*2. Operaciones matemáticas: Crea un programa que sume, reste, multiplique y
divida dos números y muestre el resultado en la consola.*/
let num1 = parseInt(prompt("Ingres un número: "));
let num2 = parseInt(prompt("Ingresa otro número"));

let suma = num1 + num2;
console.log("La suma es: " + suma);

let resta = num1 - num2;
console.log("La resta es: " + resta);

let multiplicacion = num1 * num2;
console.log("La multiplicación es: " + multiplicacion);

let division = num1 / num2;
console.log("La division es: " + division);
