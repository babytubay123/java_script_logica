//13. Número par o impar: Escribe un programa que pida un número al usuario y diga si es par o impar.
let numero = parseInt(prompt("Ingresa un número: "));
if (numero % 2 == 0) {
  alert("El número es par.");
  console.log("Es númeroes par");
} else {
  alert("El número es impar.");
  console.log("Es número impar");
}
