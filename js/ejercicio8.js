//8. Ciclo for y suma: Escribe un programa que sume todos los números del 1 al 100 usando un ciclo for .
let = prompt(
  "Este programa suma todos los números del uno al 100. Clic en aceptar"
);

let suma = 0;
for (let i = 1; i <= 100; i++) {
  suma += i;
}

console.log("Estos números sumados es igual a: " + suma);
